package numbers;

public class Rational
{
    private int numerator;
    private int denominator;

    // Default constructor
    public Rational() 
    {
        this.numerator = 0;
        this.denominator = 1;
    }
    
    public int numerator() 
    {
    	   return 0;
    }

    public int denominator() 
    {
    	  return 1;
    }

    public int getNumerator() 
    {
        return numerator;
    }

    public int getDenominator() 
    {
        return denominator;
    }
}
